package pkg;

public class Datatypes {

	public static void main(String[] args) {
		Byte a=23;
		System.out.println("Byte value of a="+a);
		short sh=345;
		System.out.println("Short value of sh="+sh);
		int num=5;
		System.out.println("Int value of num="+num);
		long l=56664l;
		System.out.println("Long value of num="+l);
		double d=563.895;
		System.out.println("Double value = "+d);
		char letter='b';
		System.out.println("Character value="+letter);
		float f=5.99f;
		System.out.println("Float value="+f);
		boolean b=50>40;
		System.out.println("Boolean="+b);
		String name="vivek";
		System.out.println("Name="+name);
				
				
		

	}

}
