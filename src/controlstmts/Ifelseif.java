public class Ifelseif {

	public static void main(String[] args) {
		int a=35,b=45,c=10;
		if(a>b&&a>c)
		{
			System.out.println("a is greater than b and c");
		}
		else if(b>a&&b>c)
		{
			System.out.println("b is greater than a and c");
		}
		else
		{
			System.out.println("c is greater than a and b");
		}
			

	}

}
